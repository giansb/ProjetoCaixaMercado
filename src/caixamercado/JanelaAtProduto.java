    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        // Atualizar
        ArrayList<Produto> lista = new ArrayList();
        lista = Produto.getProduto();
        
        try{
            for(Produto p:lista){
                if(p.getCodigo() == Integer.parseInt(jTextField1.getText())){
                    String nome = jTextField3.getText();
                    double preco = Double.parseDouble(jTextField2.getText());
                    int id = Integer.parseInt(jTextField1.getText());
                    
                    Produto produto = new Produto(id, nome, preco);
                    produto.editar();
                            
                    
                    JOptionPane.showMessageDialog(null, "Produto atualizado");
                }
                
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "codigo invalido");}
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField1.setText("");
        
    }//GEN-LAST:event_jToggleButton2ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // PROCURAR
        ArrayList<Produto> lista = new ArrayList();
        lista = Produto.getProduto();
        
        try{
            for(Produto p:lista){
                if(p.getCodigo() == Integer.parseInt(jTextField1.getText())){
                    jTextField2.setText(String.valueOf(p.getPreco()));
                    jTextField3.setText(p.getNome());
                    
                }
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "codigo invalido");}
    }//GEN-LAST:event_jToggleButton1ActionPerformed

